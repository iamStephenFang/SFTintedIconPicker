import Foundation

public struct SFDemoIconConfiguration {
    
    /// The top margin height of icon area
    public var iconAreaTopMargin = CGFloat(16)
    
    /// The horizontal margin width of icon area
    public var iconAreaHorizontalMargin = CGFloat(16)
    
    /// The height and width of icon.
    public var iconDisplaySize = CGFloat(61)
    
    /// The vertical padding height between icon and icon area.
    public var iconVerticalPadding = CGFloat(17)
}
