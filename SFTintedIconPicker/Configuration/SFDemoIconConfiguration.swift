import Foundation

public struct SFDemoIconConfiguration {
    
    /// The top margin height of icon area. Defaults to 16.
    public var topMargin = CGFloat(16)
    
    /// The horizontal margin width of icon area. Defaults to 16.
    public var horizontalMargin = CGFloat(16)
    
    /// The vertical padding height between icon and icon area. Defaults to 17.
    public var verticalPadding = CGFloat(17)
    
    /// The height and width of icon. Defaults to 61.
    public var iconSize = CGFloat(61)
    
    /// The corner radius of icon area. Defaults to 10.
    public var cornerRadius = CGFloat(10)
    
}
